fn main() {
    
    let mut x = 1;
    loop{
        x*=2;
        if x>5000
        {break;}
        println!("value of x is {}",x);
    }

    let mut y = 1;
    while y < 5000
    {
        y*=2;
        println!("value of y is {}",y);
    }

    for i in 0..5
    {
        println!("I = {}",i);
    }

    for j in 0..=5
    {
        println!("J = {}",j);
    }

    let arr =[7,8,9];

    for val in arr
    {
        println!("value = {}",val);
    }
}
